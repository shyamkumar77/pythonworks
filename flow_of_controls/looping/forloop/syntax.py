# for loop
# syntax:  for variable in range(intvalue):
                # code

for i in range(5):  # finalrange=5(5 not included)   default increment by 1
    print(i,'hai')

print("*"*100)

for i in range(5,10): # initial=5 final=10
    print(i,'hello')

print("*"*100)

for i in range(5,10,2): # initial=5  final=10  step/increment=2
    print(i,'hey')
print("*"*100)


